package datastructures;

public class ContinentMap {

	//---Object Data
	final int w, h;
	int mContinents;
	int mask;

	// ---Constructors
	public ContinentMap(int width, int height, int continents) {
		this.w = width;
		this.h = height;
		this.mContinents = continents;
		this.mask = 0;
	}

	// ---Methods
}
